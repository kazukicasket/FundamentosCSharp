namespace FundamentosCSharp;

public class operacoesMatematicas
{
    public double Adicao(double num1, double num2) => num1 + num2;

    public double Subtrair(double num1, double num2) => num1 - num2;

    public double Multiplicar(double num1, double num2) => num1 * num2;

    public double Dividir(double numerador, double denominador) => numerador / denominador;
}
